function toSwiftString(value: any): string {
  if (value === null) return 'nil'
  if (typeof value === 'string') return `"${value.replace(/"/g, '\\"')}"`
  if (typeof value === 'number') return value.toString()
  if (typeof value === 'boolean') return value ? 'true' : 'false'
  if (Array.isArray(value)) return `[${value.map(toSwiftString).join(', ')}]`
  if (typeof value === 'object') {
    const entries = Object.entries(value).map(([k, v]) => `${k}: ${toSwiftString(v)}`)
    return `[${entries.join(', ')}]`
  }
  return 'nil'
}


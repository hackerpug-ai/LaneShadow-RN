function copyFixtureFiles() {
  const fixturesDir = path.resolve(__dirname, '../sandbox/fixtures')
  const outputDir = path.resolve(
    __dirname,
    '../platforms/swift/Sources/LaneShadowMocks/Resources/fixtures',
  )

  // Ensure output directory exists
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true })
  }

  // Copy and transform fixture files
  const fixtureFiles = [
    'routes.fixtures.json',
    'users.fixtures.json',
    'sessions.fixtures.json',
    'navigator-messages.fixtures.json',
    'weather-timelines.fixtures.json',
    'planning-phases.fixtures.json',
    'suggestion-chips.fixtures.json',
  ]

  for (const fixtureFile of fixtureFiles) {
    const filepath = path.join(fixturesDir, fixtureFile)
    const content = fs.readFileSync(filepath, 'utf-8')
    const data = JSON.parse(content)

    // Split variants into separate files
    for (const [variant, items] of Object.entries(data)) {
      const baseName = fixtureFile.replace('.fixtures.json', '')
      const outputFile = path.join(outputDir, `${baseName}.${variant}.json`)
      fs.writeFileSync(outputFile, JSON.stringify(items, null, 2), 'utf-8')
    }
  }

  console.log(`✓ Copied fixture files to ${outputDir}`)
}

